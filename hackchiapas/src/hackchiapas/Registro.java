    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        String usuario="",contra="",correo="", repeatPass="";
        String tipo = (String) type.getSelectedItem();
        usuario = user.getText();
        contra = pass.getText();
        correo = mail.getText();
        repeatPass = repetirContra.getText();
        Datosconexion con = new Datosconexion ();
        
        if (repeatPass.equals(contra)) {
            try {
                if (tipo.equals("Instituci칩n")) {
                    con.institucion (usuario, contra, correo);
                    dispose ();
                    Login login = new Login ();
                    login.setVisible (true);
                    login.setLocationRelativeTo (null);
                    login.setTitle ("Iniciar Sesi칩n");
                } 
                if (tipo.equals("Tutor")) {
                    con.tutor (usuario, contra, correo);
                    dispose ();
                    Login login = new Login ();
                    login.setVisible (true);
                    login.setLocationRelativeTo (null);
                    login.setTitle ("Iniciar Sesi칩n");
                }
                if (tipo.equals("Estudiante")) {
                    con.estudiante (usuario, contra, correo);
                    dispose ();
                    Login login = new Login ();
                    login.setVisible (true);
                    login.setLocationRelativeTo (null);
                    login.setTitle ("Iniciar Sesi칩n");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Las contrase침as no coinciden, int칠ntelo nuevamente.");
        }
        
        
        
        
        
    }//GEN-LAST:event_registrarActionPerformed

